<script setup>
import { ref } from "vue";
import {
  Bars3Icon,
  BellIcon,
  Cog6ToothIcon,
  XMarkIcon,
  FingerPrintIcon,
  UsersIcon,
} from "@heroicons/vue/24/outline";
import { ChevronDownIcon, MagnifyingGlassIcon } from "@heroicons/vue/20/solid";

definePageMeta({
  middleware: "dashboard-index",
});

const route = useRoute();

const navigation = [
  {
    name: "行为验证",
    href: "/dashboard/behaviorVerification",
    icon: FingerPrintIcon,
    current: true,
  },
  { name: "团队", href: "/dashboard/team", icon: UsersIcon, current: false },
];
const userNavigation = [
  { name: "Your profile", href: "#" },
  { name: "Sign out", href: "#" },
];

const sidebarOpen = ref(false);
const commandPalettes = ref(false);

const commandPalettesShortcut = ref("K");

// 将 url 有效路径指定部分分割出来大写，之后将其赋予被选中对象。
function capitalizeFirstLetter(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

const selectedTab = ref(
  navigation.filter((val) => val.href === route.fullPath)[0].name,
);
const isSelect = (k) => selectedTab.value === k;
const selectTab = (k) => {
  selectedTab.value = k;
  sidebarOpen.value = false;
};

onMounted(() => {
  // 设置快捷键监听事件
  // 监听键盘按下事件
  window.addEventListener("keydown", function (event) {
    // 根据操作系统和按键判断
    if (
      (event.metaKey || event.ctrlKey) && // 检测 Command 键或 Ctrl 键
      event.key === commandPalettesShortcut.value.toLowerCase() // 检测 K 键
    ) {
      // 在控制台输出按下 Command+K
      // console.log("Command+K pressed");

      commandPalettes.value = !commandPalettes.value;
    }
  });
});
</script>
